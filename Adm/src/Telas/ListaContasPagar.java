    void Pesquisar(String Nome){
        
        String[] titulos={"idContasPagar","Descricao","Fornecedor","DataInicio","Vencimento","ValorLiquido","Situacao"};
        String[] Registros= new String[7];
        
        String sql="SELECT *FROM ContasPagar where Descricao LIKE '%"+Nome+"%'";
        model = new DefaultTableModel(null,titulos);
        Connection con=cc.conecta();
        
        try{         
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            
            while(rs.next()){
                
                Registros[0]=rs.getString("idContasPagar");
                Registros[1]=rs.getString("Descricao");
                Registros[2]=rs.getString("Fornecedor");
                Registros[3]=rs.getString("DataInicio");
                Registros[4]=rs.getString("Vencimento");
                Registros[5]=rs.getString("ValorLiquido");
                Registros[6]=rs.getString("Situacao");
                model.addRow(Registros);
            }           
            tabelaContasPagar.setModel(model);
            
        }catch(SQLException e){
            
            JOptionPane.showMessageDialog(null,"Erro"+e);
        }
    }

