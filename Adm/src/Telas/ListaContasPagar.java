    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        // TODO add your handling code here:
          //Botão para Somar Coluna de um jTable
        try{
            double soma=0;
            
            for(int i=0; i<tabelaContasPagar.getRowCount(); i++){
               
                soma+=Double.parseDouble(tabelaContasPagar.getValueAt(i, 4).toString());
            }
            txtTotal.setText(""+soma);
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro ao Calcular"+e.getMessage());
        }
    }//GEN-LAST:event_btnTotalActionPerformed

