        // TODO add your handling code here:
         //Botão de Excluir
         try{
             
           int Registros= tabelaContasPagar.getSelectedRow();
           String sql="DELETE FROM contaspagar WHERE idContasPagar="+tabelaContasPagar.getValueAt(Registros, 0);
           Statement st=con.createStatement();
           int n= st.executeUpdate(sql);
           
           if(n>0){
               
               Pesquisar("");
               JOptionPane.showConfirmDialog(null,"Deseja Realmente Excluir?","Excluir",JOptionPane.YES_NO_OPTION);
           }
       }catch(Exception e){
           
           JOptionPane.showMessageDialog(null,"Erro!"+e);        
       } 
