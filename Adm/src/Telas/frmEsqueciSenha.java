        props.put("mail.smtp.socketFactory.class","javax.net.ssl.SSLSocketFactory");
        props.put("mail.smtp.socketFactory.fallback","false");
        
        //Par창metros para envio Hotmail
        props.put("mail.transport.protocol", "smtp");
        props.put("mail.smtp.host", "smtp.live.com");
        props.put("mail.smtp.socketFactory.port", "465");
        props.put("mail.smtp.socketFactory.fallback", "false");
        props.put("mail.smtp.starttls.enable", "true");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.port", "465");
        
        //Par창metros para envio Yahoo
         props.put("mail.smtp.host", "smtp.mail.yahoo.com");
         props.put("mail.smtp.socketFactory.port", "465");
         props.put("mail.smtp.auth", "true");
         props.put("mail.smtp.port", "25");
        
        try{
            
           Class.forName("com.mysql.jdbc.Driver");
           Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/adm","root","");
           String sql="SELECT Senha FROM cadusuario WHERE Email LIKE '" +txtEmail.getText().trim()+"'";
           PreparedStatement stmt=con.prepareStatement(sql);
           ResultSet result= stmt.executeQuery();
           
           if(result.next()){
               
               String Senha= result.getString("Senha");
               Session session= Session.getDefaultInstance(props,null);
               session.setDebug(true);
               MimeMessage message= new MimeMessage(session);
               message.setText("A senha 챕: " +Senha);
               message.setSubject("Senha para Conta");
               message.setFrom(new InternetAddress("taiscoliveiratec@gmail.com"));
               message.setFrom(new InternetAddress("taispink10@hotmail.com"));
               message.setFrom(new InternetAddress("taiscoliveiratec@yahoo.com"));
               message.addRecipient(RecipientType.TO,new InternetAddress(txtEmail.getText().trim()));
               message.saveChanges();
               Transport transport= session.getTransport("smtp");
               transport.connect("smtp.gmail.com","taiscoliveiratec@gmail.com","Darepo12");
               transport.sendMessage(message,message.getAllRecipients());
               transport.close();
               jLabelResponse.setText("A Senha foi Enviada por Email!");   
           }
        }catch(Exception e){
            e.printStackTrace();
            jLabelResponse.setText("Endere챌o de Email n찾o Encontrado!");
        }    
