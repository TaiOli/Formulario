    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
         //Botão de Salvar
        try{
            
            String sql="INSERT INTO contaspagar(Descricao,Fornecedor,DataInicio,Vencimento,ValorLiquido,Situacao) VALUES(?,?,?,?,?,?)";
            PreparedStatement stmt= conecta.prepareStatement(sql);
            stmt.setString(1,txtDescricao.getText());
            stmt.setString(2,txtFornecedor.getText());
            stmt.setString(3,txtDataInicio.getText());
            stmt.setString(4,txtVencimento.getText());
            stmt.setDouble(5, Double.parseDouble(txtValorLiquido.getText()));
            String Situacao;
            Situacao= cbSituacao.getSelectedItem().toString();
            stmt.setString(6, Situacao);
            
            stmt.execute();
            stmt.close();
 
            JOptionPane.showMessageDialog(null,"Cadastrado com Sucesso!");

        }catch(Exception erro){
               
            JOptionPane.showMessageDialog(null,"Erro ao Cadastrar!"+erro); 
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void txtDataInicioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDataInicioFocusLost
        // TODO add your handling code here:
         SimpleDateFormat sdf= new SimpleDateFormat("dd/MM/yyyy");
        
        try{
            Date date= sdf.parse(txtDataInicio.getText());
            txtDataInicio.setValue(sdf.format(date));

        }catch(ParseException e){
            
            txtDataInicio.setFocusLostBehavior(JFormattedTextField.PERSIST);
            txtDataInicio.setText("");
            txtDataInicio.setValue(null);
        }
    }//GEN-LAST:event_txtDataInicioFocusLost

