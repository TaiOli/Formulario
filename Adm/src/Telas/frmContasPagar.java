    private void txtValorLiquidoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtValorLiquidoFocusLost
        // TODO add your handling code here:
        String sv= txtValorLiquido.getText();
        String vsf= sv.replace("R$", "").replace(" ", "").replace("", ".").replace(",", ".");
        BigDecimal Preco= new BigDecimal(vsf);
        NumberFormat nf= DecimalFormat.getCurrencyInstance(new Locale("pt","BR"));     
        String ValorFormatado= nf.format(Preco);
        txtValorLiquido.setText(ValorFormatado);
    }//GEN-LAST:event_txtValorLiquidoFocusLost

