package Javabeans;

public class Propriedades {
    
    private int idPropriedades;
    private String Predio;
    private String Andar;
    private String NAndar;
    private String Morador;
    private int Ramal;
    private String Situacao;

    public int getIdPropriedades() {
        return idPropriedades;
    }

    public void setIdPropriedades(int idPropriedades) {
        this.idPropriedades = idPropriedades;
    }

    public String getPredio() {
        return Predio;
    }

    public void setPredio(String Predio) {
        this.Predio = Predio;
    }

    public String getAndar() {
        return Andar;
    }

    public void setAndar(String Andar) {
        this.Andar = Andar;
    }

    public String getNAndar() {
        return NAndar;
    }

    public void setNAndar(String NAndar) {
        this.NAndar = NAndar;
    }

    public String getMorador() {
        return Morador;
    }

    public void setMorador(String Morador) {
        this.Morador = Morador;
    }

    public int getRamal() {
        return Ramal;
    }

    public void setRamal(int Ramal) {
        this.Ramal = Ramal;
    }
    
     public String getSituacao() {
        return Situacao;
    }

    public void setSituacao(String Situacao) {
        this.Situacao = Situacao;
    }  
}
