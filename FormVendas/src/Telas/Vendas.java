    //Metódo para Buscar Produto na base de dados
    void BuscarProduto(){
        
        String Codigo=(txtCodigo.getText());
        
        if(txtCodigo.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "Insira o Código do Produto!");
            
        }else{
            
            Produtos p=pdao.ListarId(Codigo);
            
            if(p.getCodigo()!=""){    
               
                txtid.setText(""+p.getId());
                txtDescricao.setText(p.getDescricao());
                txtPreco.setText(""+p.getPreco());
                txtEstoque.setText(""+p.getEstoque());
                              
            }else{
                JOptionPane.showMessageDialog(this, "Produto não Registrado!");
                txtCodigo.requestFocus();
            }
        }    
    }
