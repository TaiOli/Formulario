    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(txtQuantidade.getText().equals("")){
            
            JOptionPane.showMessageDialog(null,"O Campo Quantidade é Obrigatório!");
            return;
            
        }
   
        
         try{ 
                Produtos obj= new Produtos();
                obj.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
                

                ProdutosDAO dao= new ProdutosDAO();
                dao.AtualizarEstoque(obj);
                

                JOptionPane.showMessageDialog(null,"Cadastrado com Sucesso!");

           }catch(Exception erro){
               
                JOptionPane.showMessageDialog(null,"Erro ao Cadastrar!"+erro);   
           }
             
        
                DefaultTableModel modelo=(DefaultTableModel)tabelaConsultar.getModel();
                modelo.addRow(new Object[]{txtCodigo.getText(),txtDescricao.getText(),txtPreco.getText(),txtQuantidade.getText(),txtEstoque.getText(),txtTotal.getText()});  
                DefaultTableModel model=(DefaultTableModel)tabelaVenda.getModel(); 

                txtCodigo.setText("");
                txtDescricao.setText("");
                txtPreco.setText("");
                txtQuantidade.setText("");
                txtEstoque.setText(""); 
                txtTotal.setText("");
                     
       try{
            
            for(int i=0; i<tabelaConsultar.getRowCount(); i++){
               
                Preco= Double.parseDouble(tabelaConsultar.getValueAt(i, 2).toString());
                Quantidade= Integer.parseInt(tabelaConsultar.getValueAt(i, 3).toString());
                
                tPagar=Quantidade*Preco;                      
            }          
                txtTotal.setText(""+tPagar);         
        }
        catch(Exception e){
            
                JOptionPane.showMessageDialog(null, "Erro ao Calcular"+e.getMessage());
        }  
      
                model.addRow(new Object[]{txtCodigo.getText(),txtDescricao.getText(),txtPreco.getText(),txtQuantidade.getText(),txtEstoque.getText(),txtTotal.getText()});    
                txtTotal.setText(""+tPagar); 
                
       try{
            double soma=0;
            
            for(int i=0; i<tabelaVenda.getRowCount(); i++){
               
                soma+=Double.parseDouble(tabelaVenda.getValueAt(i, 5).toString());
            }
            txtTotal1.setText(""+soma);
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro ao Calcular"+e.getMessage());
        }
       
    }//GEN-LAST:event_btnAddActionPerformed

