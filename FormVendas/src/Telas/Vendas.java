        // TODO add your handling code here:
        
        for(int i=0; i<modelo.getRowCount(); i++){
            
            Produtos obj=new Produtos();
            int idP=Integer.parseInt(tabelaVenda.getValueAt(i, 1).toString());
            int Estoque=Integer.parseInt(tabelaVenda.getValueAt(i, 4).toString());
            int Quantidade=Integer.parseInt(tabelaVenda.getValueAt(i, 3).toString());
            int sa=obj.getEstoque()-Quantidade;
            pdao.AtualizarEstoque(obj);
        }
        
